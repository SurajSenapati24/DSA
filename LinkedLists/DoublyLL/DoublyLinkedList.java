package DoublyLL;
class Node{
    int data;
    Node prev;
    Node next;
    public Node(int data){
        this.data=data;
        prev=null;
        next=null;
    }
    public Node(int data, Node prev, Node next){
        this.data=data;
        this.prev=prev;
        this.next=next;
    }
}
